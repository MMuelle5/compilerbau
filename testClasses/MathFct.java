
public class MathFct {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		int zahl1 = 150;
		int zahl2 = 50;
		
		String la = "Addition: ";
		System.out.print(la);
		System.out.println(zahl1+zahl2);

		String ls = "Subtraktion: ";
		System.out.print(ls);
		System.out.println(zahl1-zahl2);

		String lm = "Multiplikation: ";
		System.out.print(lm);
		System.out.println(zahl1*zahl2);

		String ld = "Division: ";
		System.out.print(ld);
		System.out.println(zahl1/zahl2);
	}

}
